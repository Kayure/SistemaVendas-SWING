    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {                                                 
        

        String nome = txtProduto.getText();     
        Produto obj = new Produto();
        ProdutoDAO produtoDAO = new JDBCProdutoDAO(fabricaConexoes);
        obj = produtoDAO.buscarProduto(nome);
       
        if (obj.getDescricao() != null) {
             int estoque = Integer.valueOf(txtQTD.getText());
             int quantidade = Integer.valueOf(txtQTD.getText());
            precoVenda = Double.valueOf(txtPrecoVenda.getText());
            qtd = Integer.valueOf(txtQTD.getText());
            subtotal = precoVenda * qtd;
            total += subtotal;
              if(estoque >= quantidade){
            txtTotalVenda.setText(String.valueOf(total));
          
            carrinho = (DefaultTableModel) tabelaCarrinhoDeCompras.getModel();
           
            carrinho.addRow(new Object[]{
                txtCodigo.getText(),
                txtProduto.getText(),
                txtQTD.getText(),
                txtPrecoVenda.getText(),
                subtotal

            });
            }else{
            JOptionPane.showMessageDialog(null,"não foi possível adicionar no carrinho de compras \n NÃO TEMOS ESSA QUANTIDADE NO ESTOQUE!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "faltam informações inportantes a serem preenchidas! \n Verifique os dados do produto!");
            txtCodigo.requestFocus();
        }
       
        
        
    }

    private void btnFinalizarVendaActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_btnFinalizarVendaActionPerformed
        // CALCULA O TROCO 
        double dinheiro, total_venda, troco;
               

        dinheiro = Double.valueOf(txtDinheiro.getText());        
        total_venda = Double.valueOf(txtTotalVenda.getText());
        troco= dinheiro - total_venda;
        txtTroco.setText(String.valueOf(troco));

        //PEGA A DATA ATUAL
        Date agora = new Date();
        SimpleDateFormat dataEUA = new SimpleDateFormat("yyyy-MM-dd");
        String dataMysql = dataEUA.format(agora);     
        
        

        Vendas v = new Vendas();          
        
        
        
        v.setData_venda(dataMysql);
        v.setTotal_venda(total_venda);
        v.setObs(txtObs.getText());
        v.setNome_cliente(txtNome.getText());             
        v.setForma_pagamento(cbFormaPagamento.getSelectedItem().toString());

        VendaDAO vendaDAO = new JDBCVendaDAO(fabricaConexoes);
        vendaDAO.cadastrarVenda(v);
       
        
        
        
       
        
        
    }//GEN-LAST:event_btnFinalizarVendaActionPerformed

