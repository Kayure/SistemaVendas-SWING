    private void txtCpfKeyPressed(java.awt.event.KeyEvent evt) throws SQLException {
        
        //PEGAR O CPF ENQUANTO E BUSCAR O CLIENTE POR ELE
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            String cpf = txtCpf.getText();
            

            ClienteDAO clienteDAO = new JDBCClienteDAO(fabricaConexoes);
            obj = clienteDAO.buscarClienteCPF(cpf);
            if(obj.getCpf() != null){
                txtNome.setText(obj.getNome());
                txtIdCliente.setText(String.valueOf(obj.getId()));
            }else{
                JOptionPane.showMessageDialog(null,"Cliente não encontrado");
            }
        }
    }//GEN-LAST:event_txtCpfKeyPressed

