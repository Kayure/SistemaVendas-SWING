    public void listar() throws Exception {
        VendaDAO vendaDAO = new JDBCVendaDAO(fabricaConexoes);
        List<Vendas> lista = vendaDAO.listarVendaFiadas();
        DefaultTableModel dados = (DefaultTableModel) tabelaClientesConta.getModel();
        dados.setNumRows(0);

        for (Vendas v : lista) {
            dados.addRow(new Object[]{
               
                v.getId(),
                v.getNome_cliente(), 
                v.getData_venda(),     
                v.getTotal_venda(),                     
                v.getForma_pagamento(),           
            
            });
            

        }
    }

