    private void btnPesquisarVendasDiaActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_btnPesquisarVendasDiaActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate data_inicio = LocalDate.parse(txtDataInicio.getText(), formato);
        LocalDate data_fim = LocalDate.parse(txtDataFim.getText(), formato);
        VendaDAO vendaDAO = new JDBCVendaDAO(fabricaConexoes);
        List<Vendas> lista = vendaDAO.historicoVendas(data_inicio, data_fim);
        DefaultTableModel historico = (DefaultTableModel) tabelaHistorico.getModel();
        historico.setNumRows(0);
        for(Vendas v: lista){
            historico.addRow(new Object[]{
                v.getId(),
                v.getNome_cliente(),
                v.getData_venda(),
                v.getTotal_venda(),
                v.getForma_pagamento(),
            });
        }
    }//GEN-LAST:event_btnPesquisarVendasDiaActionPerformed

